package WeddingAttributes;

public class Venue {
    private String venueName;

    /**
     * Get the venue name
     * @return the name of the venue of the wedding
     */
    public String getVenueName() { return venueName; }

    /**
     * Set the wedding's venue name
     * @param venueName the name the wedding venue should be set to
     */
    public void setVenueName(String venueName){ this.venueName = venueName; }
}
